<?php
declare(strict_types = 1);

namespace SixDreams\Model;

/**
 * Class AbstractManyFieldsModel
 *
 * @method getShit();
 * @method getDog();
 * @method getFaggot();
 * @method getPenis();
 * @method getMemes();
 */
abstract class AbstractManyFieldsModel extends AbstractModel
{
    /**
     * @var null
     */
    protected $shit;

    /**
     * @var null
     */
    protected $dog;

    /**
     * @var null
     */
    protected $faggot;

    /**
     * @var null
     */
    protected $penis;

    /**
     * @var null
     */
    protected $memes;
}
